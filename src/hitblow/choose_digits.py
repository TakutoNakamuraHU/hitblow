"""開始時に桁数を選択する機能"""

def choose_digits():
    """ユーザーに 3, 4, 5 の中から桁数を選ばせて返す。"""
    print("難易度（桁数）を選択します。")
    
    while True:
        # キーボードからの入力を受け付ける
        ans = input("桁数を選んでください (3, 4, 5) > ").strip()
        
        # 入力された文字が 3, 4, 5 のいずれかであれば、数値に変換して返す
        if ans in ("3", "4", "5"):
            return int(ans)
            
        # それ以外の入力だった場合はエラーメッセージを出して再度入力させる
        print("【エラー】3, 4, 5 のいずれかを半角数字で入力してください。")